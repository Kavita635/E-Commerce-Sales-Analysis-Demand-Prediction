import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

#loading the clean dataset
df = pd.read_csv("data/cleaned_dataset.csv")
print("Cleaned datased loaded successfully!")
print(df.shape)
print(df.columns.tolist())

#converting the date columns
df["order date (DateOrders)"] = pd.to_datetime(df["order date (DateOrders)"])
df["shipping date (DateOrders)"] = pd.to_datetime(df["shipping date (DateOrders)"])
print("\nDate column converted successfully!")

#data overview
print("\nFirst 5 rows:")
print(df.head())
print("\nDataset information:")
df.info()
print("\nDescriptive Statistics:")
print(df.describe())

#sales trend over time
##Daily sales trend
daily_sales = df.groupby(df["order date (DateOrders)"].dt.date)["Sales"].sum()
plt.figure(figsize=(15,6))
plt.plot(daily_sales.index,daily_sales.values)
plt.title("Sales Trend Over Time")
plt.xlabel("Order Date")
plt.ylabel("Total Sales")
plt.xticks(rotation=45)
plt.tight_layout()
plt.savefig("images/sales_trend_over_time.png")
plt.show()

##Monthly dales trend
df["Order Month"] = df["order date (DateOrders)"].dt.to_period("M")
monthly_sales = df.groupby("Order Month")["Sales"].sum()
monthly_sales.index = monthly_sales.index.astype(str)
plt.figure(figsize=(12,5))
plt.plot(monthly_sales.index, monthly_sales.values, marker="o")
plt.title("Monthly Sales Trend")
plt.xlabel("Month")
plt.ylabel("Total Sales")
plt.xticks(rotation=45)
plt.tight_layout()
plt.savefig("images/monthly_sales_trend.png")
plt.show()
plt.close()

#top selling product by revenue (by sales)
top_sales_products = df.groupby("Product Name")["Sales"].sum()
top_sales_products = top_sales_products.sort_values(ascending=False)
print("\nTop 10 product by sales")
print(top_sales_products.head(10))
plt.figure(figsize=(12,6))
plt.bar(top_sales_products.head(10).index, top_sales_products.head(10).values)
plt.title("Top 10 products by sales revenue")
plt.xlabel("Product Name")
plt.ylabel("Total Sales")
plt.xticks(rotation=90)
plt.tight_layout()
plt.savefig("images/top_product_by_sales.png")
plt.show()
plt.close()

#top selling products by quantity
top_quantity_products = df.groupby("Product Name")["Order Item Quantity"].sum()
top_quantity_products = top_quantity_products.sort_values(ascending=False)
print("\nTop 10 products by quantity sold")
print(top_quantity_products.head(10))
plt.figure(figsize=(12,6))
plt.bar(top_quantity_products.head(10).index, top_quantity_products.head(10).values)
plt.title("Top 10 products by quantity sold")
plt.xlabel("Product Name")
plt.ylabel("Quantity Sold")
plt.xticks(rotation=90)
plt.tight_layout()
plt.savefig("images/top_products_by_quantity.png")
plt.show()
plt.close()

